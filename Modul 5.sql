-- 1. Cari berapa jumlah film yang tersedia di bioskop ini. 
SELECT COUNT(id_film) AS jumlah_film
FROM film_103122400008;

-- 2. Cari berapa jumlah film yang sedang diputar dibioskop ini. 
SELECT COUNT(DISTINCT id_film) AS jumlah_film_tayang
FROM jadwal_103122400008;

-- 3. Cari id_member yang sudah menonton lebih dari 2 kali.  
SELECT id_member, COUNT(kode_pemesanan) AS jumlah_nonton
FROM transaksi_103122400008
GROUP BY id_member
HAVING COUNT(kode_pemesanan) > 2;

-- 4. Cari member bernama Aditio dan jumlah film yang sudah ditontonnya. 
SELECT m.id_member, m.nama, m.tgl_lahir, COUNT(t.kode_pemesanan) AS jumlah_film_ditonton
FROM member_103122400008 m
LEFT JOIN transaksi_103122400008 t ON m.id_member = t.id_member
WHERE m.nama = 'Aditio'
GROUP BY m.id_member, m.nama, m.tgl_lahir;

-- 5. Cari jumlah member yang lahir untuk setiap bulannya.  
SELECT EXTRACT(MONTH FROM tgl_lahir) AS bulan, COUNT(id_member) AS jumlah_member
FROM member_103122400008
GROUP BY EXTRACT(MONTH FROM tgl_lahir)
ORDER BY bulan;

-- 6. Berapa jumlah jadwal tayang per film pada periode aktif?
SELECT f.id_film, f.judul, COUNT(j.id_jadwal) AS jumlah_jadwal
FROM film_103122400008 f
JOIN jadwal_103122400008 j ON F.id_film = J.id_film
GROUP BY F.id_film, F.judul
ORDER BY jumlah_jadwal DESC;


